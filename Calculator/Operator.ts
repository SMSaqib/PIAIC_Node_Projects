//Operators 

function Sum(num1:number,num2:number) {
    let sum=num1+num2
    return sum
};


function diff(num1:number,num2:number) {
    let sub=num1-num2
    return sub
};
function Mul(num1:number,num2:number) {
    let sum=num1*num2
    return sum
};


function div(num1:number,num2:number) {
    let sub=num1/num2
    return sub
};



export {Sum,diff,Mul,div}