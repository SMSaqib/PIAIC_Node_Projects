//Operators 
function Sum(num1, num2) {
    let sum = num1 + num2;
    return sum;
}
;
function diff(num1, num2) {
    let sub = num1 - num2;
    return sub;
}
;
function Mul(num1, num2) {
    let sum = num1 * num2;
    return sum;
}
;
function div(num1, num2) {
    let sub = num1 / num2;
    return sub;
}
;
export { Sum, diff, Mul, div };
